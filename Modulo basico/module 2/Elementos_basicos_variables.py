# Variables en python


# Declaración e inicialización de variables
edad = 28
altura = 1.65
pais = 'Mexico'

#Acceder a las variables
print('Valores inciales:')
print('Edad:',edad)
print('Altura:',altura)
print('Pais:',pais)

#Modificar el valor de una variable
edad = 30
altura = 1.68

#Acceder a las variables
print('Valores modificados:')
print('Edad:',edad)
print('Altura:',altura)
print('Pais:',pais)

#En python el tipo es dinamico
edad = 'treinta'
print('Edad:',edad) 

#Si queremos acceder a una variable no declarada va a dar error
telefono = '5544663388'
print('Telefono:', telefono)