print('*** Funcion Range ***')

print('Secuencia del 0 al 4: ')
for i in range(0, 5, 1):
    i -= 1
    print(i, end=' ')


print('\n\n Secuencia del 10 al 20: ')  
for i in range(10, 21):
    print(i, end=' ')

print('\n\n Secuencia del 20 al 30 de 2 en 2: ')  
for i in range(20, 30 + 1, 2):
    print(i, end=' ')

