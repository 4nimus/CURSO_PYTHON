#Operadores Aritmeticos

print(f'Calculadora')
a = int(input('Primero digito: '))
b = int(input('Segundo digito: '))

print(f'Suma: {a + b}')
print(f'Resta: {a - b}')
print(f'Multiplicacion: {a * b}')
print(f'Division: {a / b:2f}')
print(f'Division Entera: {a // b}')
print(f'Modulo: {a % b}')
print(f'Potencia: {a ** b}')

