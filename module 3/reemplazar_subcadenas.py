#Reemplazar subcadenas

cadena = 'Hola, Mundo!'
print(f'Cadena original: {cadena}')
nueva_cadena = cadena.replace('Mundo', 'a todos')
print(f'Nueva cadena reemplazada: {nueva_cadena}')  # Salida: Hola, a todos!

#sustituir hola por adios
nueva_cadena = cadena.replace('Hola', 'adios')
print(f'Nueva cadena reemplazada: {nueva_cadena}')  # Salidad: adios, mundo
