#Metodos de cadenas

cadena1 = 'Hola Mundo'
print(f'Cadena orginal: {cadena1}')
mayusculas = cadena1.upper()
print(f'Cadena en mayusculas: {mayusculas}') #convertir a mayusculas
minisculas = cadena1.lower()
print(f'Cadena en minusculas: {minisculas}') #convertir a minusculas
cadena2 = ' Juan perez'
print(f'Cadena con espacios: {cadena2}')
print(f'Cadena sin espacios: {cadena2.strip()}') #Eliminar espacios al incio y al final
